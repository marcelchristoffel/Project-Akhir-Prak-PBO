    private void tb_dataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_dataMouseClicked
        // TODO add your handling code here:
        int baris = tb_data.rowAtPoint(evt.getPoint());    
        String nik = tb_data.getValueAt(baris, 0).toString();
        txt_nik.setText(nik);
        String nama =tb_data.getValueAt(baris, 1).toString();
        txt_nama.setText(nama);
        String ttl =tb_data.getValueAt(baris, 2).toString();
        txt_ttl.setText(ttl);
        if("Perempuan".equals(tb_data.getValueAt(baris, 3).toString())){
            rb_perempuan.setSelected(true);
            }  else {
                    rb_laki.setSelected(true);
                    }
        String alamat =tb_data.getValueAt(baris, 4).toString();
        txt_alamat.setText(alamat);
        String rt = tb_data.getValueAt(baris, 5).toString();
        txt_rt.setText(rt);
        String rw = tb_data.getValueAt(baris, 6).toString();
        txt_rw.setText(rw);
        String kecamatan = tb_data.getValueAt(baris, 7).toString();
        txt_kecamatan.setText(kecamatan);
        String combo = tb_data.getValueAt(baris, 8).toString();
        cb_agama.setSelectedItem(combo);
        if("CERAI".equals(tb_data.getValueAt(baris, 9).toString())){
            rb_cerai.setSelected(true);
            }  else if ("KAWIN".equals(tb_data.getValueAt(baris, 9).toString())) {
                    rb_kawin.setSelected(true);
                    } else {
                            rb_belumkawin.setSelected(true);
                            }
        String pekerjaan = tb_data.getValueAt(baris, 10).toString();
        txt_pekerjaan.setText(pekerjaan);
        String kewarganegaraan = tb_data.getValueAt(baris, 11).toString();
        txt_negara.setText(kewarganegaraan);
        String masaberlaku = tb_data.getValueAt(baris, 12).toString();
        txt_masa.setText(masaberlaku);
    }//GEN-LAST:event_tb_dataMouseClicked

