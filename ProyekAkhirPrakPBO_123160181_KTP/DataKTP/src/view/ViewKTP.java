    private void bt_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_updateActionPerformed
        // TODO add your handling code here:
        try{
            String jk = null;
            if (rb_laki.isSelected()) { // Jika memilih laki laki maka akan tercentang laki laki
                jk = "Laki - laki";
            } else if (rb_perempuan.isSelected()) { // Jika memilih laki laki maka akan tercentang Perempuan
                jk = "Perempuan";
            }
            
                String status = null;
                if (rb_belumkawin.isSelected()) { // Jika memilih laki laki maka akan tercentang laki laki
                    status = "BELUM KAWIN";
                } else if (rb_kawin.isSelected()) { // Jika memilih laki laki maka akan tercentang Perempuan
                    status = "KAWIN";
                } else if (rb_cerai.isSelected()){
                    status = "CERAI";
                }
            String nik  = txt_nik.getText();
            String nama = txt_nama.getText();
            String ttl  = txt_ttl.getText();
            String rt   = txt_rt.getText();
            String rw   = txt_rw.getText();
            String alamat = txt_alamat.getText();
            String kecamatan = txt_kecamatan.getText();
            String agama = (String) cb_agama.getSelectedItem();
            String pekerjaan    = txt_pekerjaan.getText();
            String kewarganegaraan  = txt_negara.getText();
            String masaberlaku  = txt_masa.getText();

            ktp.update(nik, nama, ttl, jk, alamat, rt, rw, kecamatan, agama, status, pekerjaan, kewarganegaraan, masaberlaku);
        }catch(Exception err){
            JOptionPane.showMessageDialog(null,"Lengkapi Semua Kolom!");
        }
        DefaultTableModel model = (DefaultTableModel)this.tb_data.getModel();
        Object[] row = new Object[13];
        model.setRowCount(0);
        for (int i = 0; ktp.index()[i][0] != null; i++) {
            row[0] = ktp.index()[i][0];
            row[1] = ktp.index()[i][1];
            row[2] = ktp.index()[i][2];
            row[3] = ktp.index()[i][3];
            row[4] = ktp.index()[i][4];
            row[5] = ktp.index()[i][5];
            row[6] = ktp.index()[i][6];
            row[7] = ktp.index()[i][7];
            row[8] = ktp.index()[i][8];
            row[9] = ktp.index()[i][9];
            row[10] = ktp.index()[i][10];
            row[11] = ktp.index()[i][11];
            row[12] = ktp.index()[i][12];
            model.addRow(row);
        }
    }//GEN-LAST:event_bt_updateActionPerformed

